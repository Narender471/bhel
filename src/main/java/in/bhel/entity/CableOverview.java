package in.bhel.entity;

public class CableOverview {
	private String cableCode = "";
	private double pemQuantity = 0;
	private double storeQuantity = 0;
	private double prvQuantity = 0;
	private int pemDrums = 0;
	private int storeDrums = 0;
	private int prvDrums = 0;
	
	public String getCableCode() {
		return cableCode;
	}
	public void setCableCode(String cableCode) {
		this.cableCode = cableCode;
	}
	public double getPemQuantity() {
		return pemQuantity;
	}
	public void setPemQuantity(double pemQuantity) {
		this.pemQuantity = pemQuantity;
	}
	public double getStoreQuantity() {
		return storeQuantity;
	}
	public void setStoreQuantity(double storeQuantity) {
		this.storeQuantity = storeQuantity;
	}
	public double getPrvQuantity() {
		return prvQuantity;
	}
	public void setPrvQuantity(double prvQuantity) {
		this.prvQuantity = prvQuantity;
	}
	public int getPemDrums() {
		return pemDrums;
	}
	public void setPemDrums(int pemDrums) {
		this.pemDrums = pemDrums;
	}
	public int getStoreDrums() {
		return storeDrums;
	}
	public void setStoreDrums(int storeDrums) {
		this.storeDrums = storeDrums;
	}
	public int getPrvDrums() {
		return prvDrums;
	}
	public void setPrvDrums(int prvDrums) {
		this.prvDrums = prvDrums;
	}
	
	
	

}
