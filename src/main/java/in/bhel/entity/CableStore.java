package in.bhel.entity;

public class CableStore {
	private int sno = 0;
	private double length = 0;
	private double dbNo = 0;
	private String dbDate = "";
	private String cableCode = "";
	private String drumNo = "";
	private String uniqueCode = "";
	public int getSno() {
		return sno;
	}
	public void setSno(int sno) {
		this.sno = sno;
	}
	
	public double getLength() {
		return length;
	}
	public void setLength(double length) {
		this.length = length;
	}
	
	public double getDbNo() {
		return dbNo;
	}
	public void setDbNo(double dbNo) {
		this.dbNo = dbNo;
	}
	
	public String getDbDate() {
		return dbDate;
	}
	public void setDbDate(String dbDate) {
		this.dbDate = dbDate;
	}
	public String getCableCode() {
		return cableCode;
	}
	public void setCableCode(String cableCode) {
		this.cableCode = cableCode;
	}
	public String getDrumNo() {
		return drumNo;
	}
	public void setDrumNo(String drumNo) {
		this.drumNo = drumNo;
	}
	public String getUniqueCode() {
		return uniqueCode;
	}
	public void setUniqueCode(String uniqueCode) {
		this.uniqueCode = uniqueCode;
	}
	
	
	
	
	

}
